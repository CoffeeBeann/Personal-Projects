def Add(x,y):
    add = lambda x,y:x+y
    print(add(x,y))

def Subtract(x,y):
    subtract = lambda x,y:x-y
    print(subtract(x,y))

def Multiply(x,y):
    multiply = lambda x,y:x*y
    print(multiply(x,y))

def Divide(x,y):
    divide = lambda x,y:x/y
    print(divide(x,y))
    
def Square(x):
    square = lambda x : x**2
    print(square(x))

def Cube(x):
    cube = lambda x : x**3
    print(cube(x))

def Power(x,p):
    power = lambda x,p : x**p
    print(power(x,p))


    
    
